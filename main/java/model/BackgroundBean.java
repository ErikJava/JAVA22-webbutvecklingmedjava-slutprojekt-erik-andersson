package model;

public class BackgroundBean {
	
	int r, g, b;
	
	// Color codes for the background color, which you can change to your liking
	
	public BackgroundBean() {
		r = 255;
		g = 222;
		b = 173;
	}

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public int getG() {
		return g;
	}

	public void setG(int g) {
		this.g = g;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

}
